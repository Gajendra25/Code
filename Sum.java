import java.util.*;
public class Sum {
    public static void main (String args[]){
        Scanner ab = new Scanner(System.in);
        System.out.println("Enter two numbers -");
        int a = ab.nextInt();
        int b = ab.nextInt();
        int sum = a+b;
        System.out.println("Sum of these number : "+sum);
    }
    
}
